package org.axisgroup.paypal.payouts.response;

public class PaymentStatusResponse {
	private String orderId;
	private String paymentStatus;
	private String responseVerified;
	private String transactionId;
	private String receiverEmail;
	private String currency;
	
	public String getOrderId() {
		return orderId;
	}
	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}
	public String getPaymentStatus() {
		return paymentStatus;
	}
	public void setPaymentStatus(String paymentStatus) {
		this.paymentStatus = paymentStatus;
	}
	public String getResponseVerified() {
		return responseVerified;
	}
	public void setResponseVerified(String responseVerified) {
		this.responseVerified = responseVerified;
	}
	public String getTransactionId() {
		return transactionId;
	}
	public void setTransactionId(String transactionId) {
		this.transactionId = transactionId;
	}
	public String getReceiverEmail() {
		return receiverEmail;
	}
	public void setReceiverEmail(String receiverEmail) {
		this.receiverEmail = receiverEmail;
	}
	public String getCurrency() {
		return currency;
	}
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	
	
	
}
